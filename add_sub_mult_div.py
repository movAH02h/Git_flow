import math

def add(a, b):
    return a + b

def subtraction(a, b):
    return a - b

def multiple(a, b):
    return a * b

def div(a, b):
    return a // b

def logariphm_two(a):
    return math.log(a, 2)

def logariphm_natural(a):
    return math.log(a)

def sqrt(a):
    return math.sqrt(a)

def sqrt3(a):
    return a ** (1 / 3)

def sqrtN(a, n):
    return a ** (1 / n)



