import math

def add(a, b):
    return a + b


def subtraction(a, b):
    return a - b


def multiple(a, b):
    return a * b


def div(a, b):
    return a // b


def logariphm_two(a):
    return math.log(a, 2)


def logariphm_natural(a):
    return math.log(a)


